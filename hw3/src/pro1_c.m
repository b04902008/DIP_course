time = 0;
cnt = 1;
while cnt ~= 0
    time = time + 1;
    img_conditional = zeros(X, Y);
    % stage 1
    img_pad = padarray(img_skeletonized, [1 1], 0, 'both');
    mask = [16 32 64; 8 256 128; 4 2 1];
    for i=1:X
        for j=1:Y
            pattern = sum(sum(img_pad(i:i+2, j:j+2) .* mask));
            if conMarkPattern(pattern + 1) == 1
                img_conditional(i, j) = 1;
            end
        end
    end
    % stage 2
    img_pad = padarray(img_conditional, [1 1], 0, 'both');
    mask = [16 32 64; 8 256 128; 4 2 1];
    for i=1:X
        for j=1:Y
            pattern = sum(sum(img_pad(i:i+2, j:j+2) .* mask));
            if unconMarkPattern(pattern + 1) == 1
                img_conditional(i, j) = 0;
            end
        end
    end
    cnt = sum(sum(img_conditional));
    img_skeletonized = img_skeletonized - img_conditional;
end
